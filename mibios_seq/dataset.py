from mibios.dataset import Dataset
from mibios.utils import getLogger

from .models import Sequencing


log = getLogger(__name__)


class FastqFileData(Dataset):
    """
    Table generated by fastq-scraper script
    """
    name = 'fastq_file_data'
    model = Sequencing

    fields = (
        ('name', 'name'),
        ('r1_file', 'r1'),
        ('r2_file', 'r2'),
        ('run__serial', 'run__serial'),
        ('run__number', 'run__number'),
        ('snumber', 'snumber'),
    )
